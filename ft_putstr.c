#include "ft_printf.h"

static int	ft_strlen(char *str)
{
	int	ind;

	ind = 0;
	while (str[ind] != '\0')
		ind++;
	return (ind);
}

int	ft_putstr(char *s)
{
	if (s == NULL)
		return (write(1, "(null)", 6));
	return (write(1, s, ft_strlen(s)));
}
